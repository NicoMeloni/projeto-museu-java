    private void btnSelecionarImgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarImgActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Imagens", "jpg", "jpeg", "png", "gif");
        fileChooser.setFileFilter(filter);
        
        int valor = fileChooser.showOpenDialog(null);
        
        if (valor == JFileChooser.APPROVE_OPTION) {
            this.imagemSelecionada = fileChooser.getSelectedFile();
            String nomeImagem = this.imagemSelecionada.getName();
            String extensao = nomeImagem.substring(nomeImagem.lastIndexOf(".") + 1).toLowerCase();
            if (extensao.equals("jpg") || extensao.equals("jpeg") || extensao.equals("png") || extensao.equals("gif")) {
                JOptionPane.showMessageDialog(null, "Imagem selecionada: " + imagemSelecionada.getAbsolutePath());
            } else{
                JOptionPane.showMessageDialog(null, "Por favor, selecione um arquivo de imagem válido.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Nenhuma imagem selecionada.");
        }
    }//GEN-LAST:event_btnSelecionarImgActionPerformed

