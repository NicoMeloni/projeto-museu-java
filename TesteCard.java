    public void carregarTabelaObras(ArrayList<Obra> a){
        DefaultTableModel modelo = new DefaultTableModel(new Object[] {"Registro","Nome da Obra","Autor","Doador","Data Aquisicão", "Preço Aquisição", "Sala"}, 0);
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");

        for (Obra obra : a){
            Object linha[] = new Object[] {obra.getId(),obra.getNomeObra(),obra.getAutor(),obra.getDoador(),obra.getDataAquisicao().format(formato),obra.getPrecoAquis(), "--"};
            
            modelo.addRow(linha);
        }
        
        tabelaObras.setModel(modelo);

        
    }
