    private void btnPesquisarObraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarObraActionPerformed
        auxiliarObra = "pesquisar";
        ArrayList<Obra> pesquisa = new ArrayList<>();
        
        if (txtPesquisaObra.getText().equals("") && txtPesquisaRegistro.getText().equals("") &&
            txtPesquisaAutor.getText().equals("") && txtPesquisaDoador.getText().equals("")){
            
            JOptionPane.showMessageDialog(null, "Insira pelo menos um campo de pesquisa.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        } 
        else{
            if (!txtPesquisaObra.getText().equals("") ){
                for (Obra obra : Obra.getListaObras()){
                    if (obra.getNomeObra().equals(txtPesquisaObra.getText())){
                        pesquisa.add(obra);
                    }
                }
            }
            
            if(!txtPesquisaRegistro.getText().equals("")){
                try{
                    int id = Integer.parseInt(txtPesquisaRegistro.getText());
                    for (Obra obra : Obra.getListaObras()){
                        if(obra.getId() == id){
                            pesquisa.add(obra);
                        }
                    }
                }
                catch(NumberFormatException e){
                    JOptionPane.showMessageDialog(null, "O registro precisa ser um número.", "Mensagem", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }
            
            if(!txtPesquisaAutor.getText().equals("") && txtPesquisaDoador.getText().equals("")){
                for(Obra obra : Obra.getListaObras()){
                    if(txtPesquisaAutor.getText().equals(obra.getAutor())){
                        pesquisa.add(obra);
                    }
                }
            }
            
            if(!txtPesquisaDoador.getText().equals("") && txtPesquisaAutor.getText().equals("")){
                for(Obra obra : Obra.getListaObras()){
                    if(txtPesquisaDoador.getText().equals(obra.getDoador())){
                        pesquisa.add(obra);
                    }
                }
            }
            
            if(!txtPesquisaDoador.getText().equals("") && !txtPesquisaAutor.getText().equals("")){
                for(Obra obra1 : Obra.getListaObras()){
                    for(Obra obra2 : Obra.getListaObras()){
                        if((txtPesquisaDoador.getText().equals(obra1.getDoador()) && 
                            txtPesquisaAutor.getText().equals(obra1.getAutor())) &&
                            txtPesquisaDoador.getText().equals(obra2.getDoador()) &&
                            txtPesquisaAutor.getText().equals(obra2.getAutor())){
                            if(!pesquisa.contains(obra1)){
                                pesquisa.add(obra1);
                            }
                        }
                    }
                }
            }
        }
        
        if (pesquisa.isEmpty()){
            JOptionPane.showMessageDialog(null, "Nenhum resultado encontrado.", "Mensagem", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            carregarTabelaObras(pesquisa);
        }
    }//GEN-LAST:event_btnPesquisarObraActionPerformed

